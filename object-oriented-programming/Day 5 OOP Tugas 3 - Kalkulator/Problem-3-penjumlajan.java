public class penjumlajan {
    int angka1;
    int angka2;

    public int caramenjumlahkan() {
        int hasil = angka1 + angka2;
        return hasil;
    }

    public void setAngka1(int angka1) {
        this.angka1 = angka1;
    }

    public void setAngka2(int angka2) {
        this.angka2 = angka2;
    }

    public int getAngka1() {
        return angka1;
    }

    public int getAngka2() {
        return angka2;
    }
}
